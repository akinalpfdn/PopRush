package com.akinalpfdn.poprush.core.domain.model

/**
 * Represents the different shapes that bubbles can have in the game.
 */
enum class BubbleShape {
    /** Perfect circle shape */
    CIRCLE,

    /** Rounded square shape */
    SQUARE,

    /** Perfect triangle shape */
    TRIANGLE,

    /** Five-pointed star shape */
    STAR,

    /** Perfect heart shape */
    HEART
}